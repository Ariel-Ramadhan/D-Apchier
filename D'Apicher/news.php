        <h1>Chainsaw Man Figurine Captures the Devil Hunter's Every Gory Detail</h1>
        <p class="subTitle">A gore-covered and bloody figure of Chainsaw Man's protagonist, Denji, is available for preorder ahead of the anime adaptation's upcoming release.</p>
        <h5>PUBLISHED OCT 20, 2022</h5>
        <img src="https://static1.cbrimages.com/wordpress/wp-content/uploads/2022/08/chainsaw-man-figure-sideshow.jpg" alt="denjicm" width="1140px">
        <p class="isi">The blood and violence of Chainsaw Man has been captured in gruesomely dimensional detail, thanks to an upcoming figure. <br>
        <br>
        eStream has unveiled its Shibuya Scramble Figure series with a fascinatingly brutal depiction of Chainsaw Man himself, which is available for preorder on Sideshow, retailing for a price of $516. The 1:7 scale statue stands at 11" tall and showcases series protagonist Denji in his hybrid form standing atop a pile of brutalized corpses. Gore and viscera cling to his chainsaw appendages and blood explodes from all angles, perfectly invoking the tone of Tatsuki Fujimoto's original manga.</p>
