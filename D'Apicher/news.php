        <p class="isi">Fujimoto first published the manga series in Shueisha's Weekly Shōnen Jump from December 2018 until December 2020, while the second part premiered in the Shōnen Jump+ online magazine earlier this year. The narrative focuses on Denji, an impoverished youth who is struggling to escape from the mountainous debt left behind by his deceased father. With the help of a dog-like entity named Pochita, the young man uses his ability to transform parts of his body into chainsaws to kill devils on behalf of gangsters. However, when he is betrayed by Yakuza employers, Denji is recruited by the Public Safety Devil Hunters, a government agency that battles against monstrous threats. <br>
        <br>
        As eStream's new figure illustrates, the Chainsaw Man manga is overflowing with explicit content, which will seemingly carry over into MAPPA's highly anticipated anime adaptation. This was promised at a recent panel for the new anime when one of the producers stated, "We are not censoring anything. We want to be true to the source material." The ultra-violence was further confirmed by Chainsaw Man's recently released trailer, which features a plethora of dead bodies within its first couple of seconds. <br>
        <br>
        The upcoming anime series is being directed by Ryu Nakayama, who previously worked on classics like Sword Art Online and Fate/Grand Order - Absolute Demonic Front: Babylonia, and written by Hiroshi Seko, who was one of the writers behind the Vinland Saga and Jujutsu Kaisen anime adaptations. Several members of the voice cast have finally been revealed, including Kikunosuke Toya (Tokyo 24th Ward) as Denji, Fairouz Ai (Is It Wrong to Try to Pick Up Girls in a Dungeon?) as Power, Shogo Sakata (Suppose a Kid From the Last Dungeon Boonies Moved to a Starter Town) as Aki Hayakawa and Tomori Kusunoki (Love Live! Nijigasaki High School Idol Club) as Makima. <br>
        <br>
        The Chainsaw Man figure is expected to begin shipping in December 2023 and the anime adaptation is set to premiere this October.
